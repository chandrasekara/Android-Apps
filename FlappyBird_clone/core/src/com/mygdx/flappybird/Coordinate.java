public class Coordinate {

    private int xPos;
    private int yPos;

    public Coordinate(int x_in, int y_in) {

        xPos = x_in;
        yPos = y_in;

    }

    public int getXPos() {
        return xPos;
    }

    public int getYPos() {
        return yPos;
    }


}
